    public static int[] dailyTemperatures1(int[] T) {
        int length = T.length;
        int[] result = new int[length];
        for (int i = 0; i < length; i++) {
            for (int j = i + 1; j < length; j++) {
                if (T[j] > T[i]) {
                    result[i] = j - i;
                    break;
                }
            }
        }
        return result;
    }


    public static int[] dailyTemperatures2(int[] T) {
        Stack<Integer> stack = new Stack<>();
        int length = T.length;
        int[] result = new int[length];

        for (int i = 0; i < length; i++) {
            while (!stack.isEmpty()) {
                if (T[i] <= T[stack.peek()]) {
                    break;
                } else {
                    int pre = stack.pop();
                    result[pre] = i - pre;
                }
            }
            stack.add(i);
        }
        return result;
    }


